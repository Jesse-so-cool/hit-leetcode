    //Set<String> set = new HashSet();
    int k = 0;
    int x = 0;
    int y = 0;
    static String split = "-";
    public int numIslands(char[][] grid) {
        if (grid.length == 0)
            return 0;
        x = grid.length;
        y = grid[0].length;
        Set<String> track = new HashSet();
        for (int i = 0; i < x; i++) {
            for (int j = 0; j < y; j++) {
                if (grid[i][j] == '1') {
                    if (!track.contains(i + split + j)) {
                        track.add(i + split + j);
                        dfs(grid, i, j, track);
                        k++;
                        //break;
                    }
                }
            }
        }
        return k;
    }

    private void dfs(char[][] grid, int row, int col, Set<String> set) {
        if ((row > x - 1 || col > y - 1)
                || (row < 0 || col < 0)
                || (grid[row][col] == '0')) {
            set.remove(row + split + col);
            return;
        }
        String right = row + split + (col + 1);
        if (!set.contains(right)) {
            set.add(right);
            dfs(grid, row, col + 1, set);
        }

        String down = (row + 1) + split + (col);
        if (!set.contains(down)) {
            set.add(down);
            dfs(grid, row + 1, col, set);
        }
        String left = row + split + (col - 1);
        if (!set.contains(left)) {
            set.add(left);
            dfs(grid, row, col - 1, set);
        }
        String up = (row - 1) + split + (col);
        if (!set.contains(up)) {
            set.add(up);
            dfs(grid, row - 1, col, set);
        }
    }
